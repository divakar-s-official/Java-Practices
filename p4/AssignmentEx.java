package p4;

public class AssignmentEx {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a+=5); // a = a +5  a = 5 +5 = 10
        System.out.println(a-=5);
        System.out.println(a*=5);
        System.out.println(a/=5);
        System.out.println(a%=5);
        System.out.println(a);
    }
}
